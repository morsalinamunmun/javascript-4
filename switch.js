const color = 'yellow';
if(color === 'green'){
    console.log('green');
}else if(color === 'red'){
    console.log('red');
}else if(color === 'yellow'){
    console.log('yellow');
}else if(color === 'blue'){
    console.log('blue');
}else{
    console.log('black');
}

//switch
switch(color){
    case 'green':
    console.log('green');
    break;
    case 'red':
    console.log('red');
    break;
    case 'yellow':
    console.log('yellow');
    break;
    case 'blue':
    console.log('blue');
    break;
    default:
        console.log('black');
}